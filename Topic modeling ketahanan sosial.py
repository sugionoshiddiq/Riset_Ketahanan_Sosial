# ════════════════════════════════════════════════════════════
# 4. PERSIAPAN COHERENCE
# ════════════════════════════════════════════════════════════
def tokenize(text):
    text   = text.lower()
    text   = re.sub(r'[^a-z\s]', ' ', text)
    tokens = [t for t in text.split() if len(t) > 2]
    return tokens

tokenized_docs = [tokenize(d) for d in docs]
gensim_dict    = Dictionary(tokenized_docs)

def compute_coherence(topic_model, tokenized, dictionary):
    topic_words = []
    for _, row in topic_model.get_topic_info().iterrows():
        tid = row['Topic']
        if tid == -1:
            continue
        words = [w for w, _ in topic_model.get_topic(tid)
                 if w in dictionary.token2id]
        if words:
            topic_words.append(words)
    if not topic_words:
        return 0.0
    cm = CoherenceModel(
        topics=topic_words,
        texts=tokenized,
        dictionary=dictionary,
        coherence='c_v'
    )
    return cm.get_coherence()

# ════════════════════════════════════════════════════════════
# 5. EKSPERIMENTASI min_cluster_size
# ════════════════════════════════════════════════════════════
umap_model = UMAP(
    n_neighbors=15,
    n_components=5,
    min_dist=0.0,
    metric='cosine',
    random_state=42
)

vectorizer_model = CountVectorizer(
    stop_words='english',
    min_df=3,
    ngram_range=(1, 2)
)

min_cluster_sizes = [5, 10, 15, 20, 25]
results = []
models  = {}

print("=" * 65)
print(f"{'min_cluster_size':>18} | {'n_topics':>8} | {'coherence_cv':>13} | {'outlier_%':>10}")
print("=" * 65)

for mcs in min_cluster_sizes:
    hdbscan_model = HDBSCAN(
        min_cluster_size=mcs,
        min_samples=1,
        metric='euclidean',
        cluster_selection_method='eom',
        prediction_data=True
    )

    topic_model = BERTopic(
        umap_model=umap_model,
        hdbscan_model=hdbscan_model,
        vectorizer_model=vectorizer_model,
        embedding_model=embedding_model,
        top_n_words=10,
        verbose=False
    )

    topics, probs = topic_model.fit_transform(docs, embeddings)

    n_topics    = len(set(topics)) - (1 if -1 in topics else 0)
    outlier_pct = topics.count(-1) / len(topics) * 100
    coh         = compute_coherence(topic_model, tokenized_docs, gensim_dict)

    models[mcs] = {
        'model'      : topic_model,
        'topics'     : topics,
        'probs'      : probs,
        'n_topics'   : n_topics,
        'coherence'  : coh,
        'outlier_pct': outlier_pct
    }
    results.append({
        'min_cluster_size': mcs,
        'n_topics'        : n_topics,
        'coherence_cv'    : round(coh, 4),
        'outlier_pct'     : round(outlier_pct, 2)
    })

    print(f"{mcs:>18} | {n_topics:>8} | {coh:>13.4f} | {outlier_pct:>9.2f}%")

print("=" * 65)

