warnings.filterwarnings('ignore')

# ════════════════════════════════════════════════════════════
# 1. LOAD DATA
# ════════════════════════════════════════════════════════════
df = pd.read_excel('/content/sample_data/scopus_full_tahap 2.xlsx')
print(f"Shape awal   : {df.shape}")
print(f"Kolom        : {df.columns.tolist()}\n")

# ════════════════════════════════════════════════════════════
# 2. PREPROCESSING — GABUNG TITLE + ABSTRACT
# ════════════════════════════════════════════════════════════
def combine_text(row):
    title    = str(row['title'])    if pd.notna(row['title'])    else ''
    abstract = str(row['abstract']) if pd.notna(row['abstract']) else ''
    return (title + ' ' + abstract).strip() or None

df['text'] = df.apply(combine_text, axis=1)
df['year'] = pd.to_numeric(df['year'], errors='coerce')

df_clean = df[
    df['text'].notna() &
    (df['text'].str.len() > 20) &
    df['year'].notna()
].reset_index(drop=True)

docs       = df_clean['text'].tolist()
timestamps = df_clean['year'].astype(int).tolist()

print(f"Dokumen valid : {len(docs)}")
print(f"Rentang tahun : {min(timestamps)} – {max(timestamps)}\n")

# ════════════════════════════════════════════════════════════
# 3. EMBEDDING — all-mpnet-base-v2
# ════════════════════════════════════════════════════════════
print("Loading model: all-mpnet-base-v2 ...")
embedding_model = SentenceTransformer('all-mpnet-base-v2')

device = 'cuda' if torch.cuda.is_available() else 'cpu'
print(f"Device        : {device}")

print("Encoding dokumen (beberapa menit)...")
embeddings = embedding_model.encode(
    docs,
    show_progress_bar=True,
    batch_size=32,
    device=device
)
